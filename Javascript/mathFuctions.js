//? La méthode Math.ceil()
//* Situation : Arrondir un nombre à l'entier supérieur.
//* Exemple :
let numCeil = 2.3;
const roundedUp = Math.ceil(numCeil);
console.log("⭐ Math.ceil example :", { roundedUp });

//? -------------------------------------------------

//? La méthode Math.floor()
//* Situation : Arrondir un nombre à l'entier inférieur.
//* Exemple :
let numFloor = 2.8;
const roundedDown = Math.floor(numFloor);
console.log("⭐ Math.floor example :", { roundedDown });

//? -------------------------------------------------

//? La méthode Math.round()
//* Situation : Arrondir un nombre à l'entier le plus proche.
//* Exemple :
let numRound = 2.5;
const rounded = Math.round(numRound);
console.log("⭐ Math.round example :", { rounded });

//? -------------------------------------------------

//? La méthode Math.max()
//* Situation : Trouver le plus grand nombre parmi les arguments donnés.
//* Exemple :
const max = Math.max(1, 3, 2);
console.log("⭐ Math.max example :", { max });

//? -------------------------------------------------

//? La méthode Math.min()
//* Situation : Trouver le plus petit nombre parmi les arguments donnés.
//* Exemple :
const min = Math.min(1, 3, 2);
console.log("⭐ Math.min example :", { min });

//? -------------------------------------------------

//? La méthode Math.pow()
//* Situation : Calculer une puissance d'un nombre.
//* Exemple :
const power = Math.pow(2, 3); // 2^3
console.log("⭐ Math.pow example :", { power });

//? -------------------------------------------------

//? La méthode Math.sqrt()
//* Situation : Calculer la racine carrée d'un nombre.
//* Exemple :
const sqrt = Math.sqrt(9); // Racine carrée de 9
console.log("⭐ Math.sqrt example :", { sqrt });

//? -------------------------------------------------

//? La méthode Math.random()
//* Situation : Générer un nombre aléatoire entre 0 et 1.
//* Exemple :
const randomNum = Math.random();
console.log("⭐ Math.random example :", { randomNum });

